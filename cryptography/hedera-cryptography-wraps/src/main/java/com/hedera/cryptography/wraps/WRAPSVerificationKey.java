    /**
     * The current key that the WRAPS verification API is using, and it MUST BE initialized with a clone() of
     * the DEFAULT_KEY because we never want to allow anyone to modify the content of the DEFAULT_KEY.
     */
    private static byte[] currentKey = DEFAULT_KEY.clone();

    private WRAPSVerificationKey() {}

    /**
     * Get a copy of the default WRAPS verification key that is currently hard-coded in the library.
     * @return a copy of the default key
     */
    public static byte[] getDefaultKey() {
        return DEFAULT_KEY.clone();
    }

    /**
     * Get a reference to the current WRAPS verification key that the library is using now. For performance reasons,
     * this method doesn't create a defensive copy of the current key, so the client code MUST NOT modify the array.
     * @return an array containing the currently used key
     */
    public static byte[] getCurrentKey() {
        return currentKey;
    }

    /**
     * Set the current WRAPS verification key. This method doesn't create a defensive copy of the given array,
     * so the client code is fully responsible for not modifying the array after passing it to this method.
     * The client code is also responsible for thread safety with respect to calling verification APIs, such as
     * `WRAPSLibraryBridge.verifyCompressedProof()` or `TSS.verifyTSS()`, that use this key.
     * @param key a WRAPS verification key that should be used by the library
     * @throws IllegalArgumentException if the key length isn't 1768 bytes
     */
    public static void setCurrentKey(byte[] key) throws IllegalArgumentException {
        if (key == null || key.length != 1768) {
            throw new IllegalArgumentException("The WRAPS verification key must be 1768 bytes, instead got "
                    + (key == null ? "null" : key.length));
        }
        currentKey = key;
    }
